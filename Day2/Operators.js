// Arithmetic Operators
let a = 10;
let b = 3;

console.log("Addition:", a + b);          
console.log("Subtraction:", a - b);       
console.log("Multiplication:", a * b);    
console.log("Division:", a / b);          
console.log("Modulus:", a % b);           
console.log("Exponentiation:", a ** b);   

// Assignment Operators
let x = 5;
x += 3;  
console.log("Assignment (+=):", x);      

// Comparison Operators
console.log("Equal:", a == b);            // false
console.log("Strict Equal:", a === b);    // false
console.log("Not Equal:", a != b);        // true
console.log("Greater Than:", a > b);      // true
console.log("Less Than or Equal:", a <= b); // false

// Logical Operators
let isTrue = true;
let isFalse = false;

console.log("AND:", isTrue && isFalse);   // false
console.log("OR:", isTrue || isFalse);    // true
console.log("NOT:", !isTrue);             // false

// String Concatenation Operator
let firstName = "Palak";
let lastName = "Jain";
console.log("Full Name:", firstName + " " + lastName); // Palak Jain
