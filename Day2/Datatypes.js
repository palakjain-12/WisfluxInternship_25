// String
let name = "Palak";
console.log("Name:", name);

// Number
let age = 20;
console.log("Age:", age);

// Boolean
let isStudent = true;
console.log("Is Student:", isStudent);

// Null
let middleName = null;
console.log("Middle Name:", middleName);

// Undefined
let nickname;
console.log("Nickname:", nickname);

// Object
let person = { name: "Palak", age: 20 };
console.log("Person:", person);

// Array
let colors = ["red", "green", "blue"];
console.log("Colors:", colors);

// Function
let greet = function () {
    return "Hello!";
};
console.log("Greeting:", greet());

// Symbol
let id = Symbol("id");
console.log("Symbol ID:", id);

// BigInt
let bigNumber = 123456789012345678901234567890n;
console.log("BigInt:", bigNumber);


// VAR
var city = "Jaipur";
console.log("City (var):", city);
var city = "Delhi"; // Redeclaration allowed
console.log("City after redeclaration (var):", city);

// LET
let state = "Rajasthan";
console.log("State (let):", state);
// let state = "Gujarat"; //  Redeclaration not allowed

// CONST
const country = "India";
console.log("Country (const):", country);
// country = "USA"; //  Reassignment not allowed
